
package com.safire.model;

public class Colores {
    int cod_color;
    String nombre_color, cod_usuario, fecha_creacion;

    public int getCod_color() {
        return cod_color;
    }

    public void setCod_color(int cod_color) {
        this.cod_color = cod_color;
    }

    public String getNombre_color() {
        return nombre_color;
    }

    public void setNombre_color(String nombre_color) {
        this.nombre_color = nombre_color;
    }

    public String getCod_usuario() {
        return cod_usuario;
    }

    public void setCod_usuario(String cod_usuario) {
        this.cod_usuario = cod_usuario;
    }

    public String getFecha_creacion() {
        return fecha_creacion;
    }

    public void setFecha_creacion(String fecha_creacion) {
        this.fecha_creacion = fecha_creacion;
    }
    
}
